                to implement the database in my site took a good amount of time to learn, and I felt like I had to rush a bit to finish 
                because of it. Next time, I’ll be more generous with how long I give myself to fully understand a concept in respect to 
                the deadline I have to finish the project I need to apply it to.</p>
