        <div class="section">
            <h2>The Solution</h2>
            <a href="http://parkerishere.com/IDM232"><p>Link to the final product</p></a>
            <p>My final site opens to a homepage. “All recipes” takes you to a list of the recipes on the page, and clicking on one 
                will show you more details on the recipe. There’s a button on the details page to edit an item, and clicking on that 
                takes you to a page where you can edit the title, description, ingredients, and steps of each page. At the bottom is 
                a button to delete recipes, which will permanently remove them. You can also add new recipes at the bottom of the page 
                using the “New Recipe” button, and the page for that is about the same as the page for editing recipes.</p>
        </div>
        <div class="section">
            <h2>Results</h2>
            <p>In the end, I found that my project was successful. My page is capable of adding, viewing, editing, and deleting recipes 
                and everything on my page functions as expected. In addition, it has a very simplified feel that offers the user a 
                layout that doesn’t feel like it has no breathing room. Many of the family and friends that I showed the page to noted 
                that the site felt very clean, but also inviting and warm. This indicates to me that I’ve been successful in my goals.
                I also learned a lot in this process. Of course, I learned how to code in PHP and the interworkings of back-end 
                development. This was the thing I was looking most forward to when I began this project, and I feel like I’ve gained a 
